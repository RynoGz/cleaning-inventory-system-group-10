    private void btnAddIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddIActionPerformed
         try {
            Issue issuance = new Issue();

            issuance.setMaterialId(Integer.parseInt(txtMaterialIdI.getText().trim()));
            issuance.setCleanerId(Integer.parseInt(txtCleanerIdI.getText().trim()));
            issuance.setQuantityIssued(Integer.parseInt(txtQuantityI.getText().trim()));

            int newId = issueController.addIssuance(issuance);

            if (newId > 0) {
                JOptionPane.showMessageDialog(this, "Issuance created with ID: " + newId);
                loadIssuances();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to create issuance.");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Material ID, Cleaner ID and Quantity must be numbers.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddIActionPerformed

    private void btnUpdateIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateIActionPerformed
    
        try {
            if (selectedIssuanceId == -1) {
                JOptionPane.showMessageDialog(this, "Please select an issuance first.");
                return;
            }

            Issue issuance = new Issue();
            issuance.setIssuanceId(selectedIssuanceId);
            issuance.setMaterialId(Integer.parseInt(txtMaterialIdI.getText().trim()));
            issuance.setCleanerId(Integer.parseInt(txtCleanerIdI.getText().trim()));
            issuance.setQuantityIssued(Integer.parseInt(txtQuantityI.getText().trim()));

            if (issueController.updateIssuance(issuance)) {
                JOptionPane.showMessageDialog(this, "Issuance updated successfully.");
                loadIssuances();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update issuance.");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateIActionPerformed

    private void btnDeleteIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteIActionPerformed
    
        if (selectedIssuanceId == -1) {
            JOptionPane.showMessageDialog(this, "Please select an issuance first.");
            return;
        }

        int choice = JOptionPane.showConfirmDialog(this, "Delete this issuance?", "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (choice == JOptionPane.YES_OPTION) {
            try {
                if (issueController.deleteIssuance(selectedIssuanceId)) {
                    JOptionPane.showMessageDialog(this, "Issuance deleted.");
                    loadIssuances();
                    clearFields();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete issuance.");
                }

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnDeleteIActionPerformed

