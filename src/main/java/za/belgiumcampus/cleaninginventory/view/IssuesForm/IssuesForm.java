    private void loadIssuances() {
        try {
            tableModel.setRowCount(0);
            List<Issue> list = issueController.getAllIssuances();

            for (Issue i : list) {
                tableModel.addRow(new Object[]{
                    i.getIssuanceId(),
                    i.getMaterialId(),
                    i.getCleanerId(),
                    i.getQuantityIssued(),
                    i.getCreatedAt()
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }
     private void clearFields() {
        txtMaterialIdI.setText("");
        txtCleanerIdI.setText("");
        txtQuantityI.setText("");
        txtSearchI.setText("");
        selectedIssuanceId = -1;
        txtMaterialIdI.requestFocus();
    }

    private void searchIssuances() {
        try {
            tableModel.setRowCount(0);
            String searchText = txtSearchI.getText().trim();
            List<Issue> list = issueController.searchIssuances(searchText);

            for (Issue i : list) {
                tableModel.addRow(new Object[]{
                    i.getIssuanceId(),
                    i.getMaterialId(),
                    i.getCleanerId(),
                    i.getQuantityIssued(),
                    i.getCreatedAt()
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }
