    private void loadCleaners() {
        try {
            tableModel.setRowCount(0);
            List<Cleaner> list = cleanerController.getAllCleaners();

            for (Cleaner c : list) {
                tableModel.addRow(new Object[]{
                    c.getCleanerId(),
                    c.getFirstName(),
                    c.getLastName(),
                    c.getEmail(),
                    c.getPhone(),
                    c.getDepartment(),
                    c.getIsActive()
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }
        
        private void clearFields() {
        txtFirstName.setText("");
        txtLastName.setText("");
        txtEmail.setText("");
        txtPhone.setText("");
        cmbDepartment.getSelectedItem().toString();
        chkActive.setSelected(false);
        txtSearch.setText("");
        selectedCleanerId = -1;
        txtFirstName.requestFocus();
    }

    private void searchCleaners() {
        try {
            tableModel.setRowCount(0);
            String searchText = txtSearch.getText().trim();
            List<Cleaner> list = cleanerController.searchCleaners(searchText);

            for (Cleaner c : list) {
                tableModel.addRow(new Object[]{
                    c.getCleanerId(),
                    c.getFirstName(),
                    c.getLastName(),
                    c.getEmail(),
                    c.getPhone(),
                    c.getDepartment(),
                    c.getIsActive()
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }


