    private void btnDeleteCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCActionPerformed
    if (selectedCleanerId == -1) {
            JOptionPane.showMessageDialog(this, "Please select a cleaner first.");
            return;
        }

        int choice = JOptionPane.showConfirmDialog(this, "Delete this cleaner?", "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (choice == JOptionPane.YES_OPTION) {
            try {
                if (cleanerController.deleteCleaner(selectedCleanerId)) {
                    JOptionPane.showMessageDialog(this, "Cleaner deleted.");
                    loadCleaners();
                    clearFields();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete cleaner.");
                }

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteCActionPerformed

    private void tblCleanerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCleanerMouseClicked
    
            int row = tblCleaner.getSelectedRow();
    selectedCleanerId = Integer.parseInt(tableModel.getValueAt(row, 0).toString());

    txtFirstName.setText(tableModel.getValueAt(row, 1).toString());
    txtLastName.setText(tableModel.getValueAt(row, 2).toString());
    txtEmail.setText(tableModel.getValueAt(row, 3).toString());
    txtPhone.setText(tableModel.getValueAt(row, 4).toString());

    cmbDepartment.setSelectedItem(tableModel.getValueAt(row, 5).toString());

    chkActive.setSelected(Boolean.parseBoolean(tableModel.getValueAt(row, 6).toString()));
    }//GEN-LAST:event_tblCleanerMouseClicked

