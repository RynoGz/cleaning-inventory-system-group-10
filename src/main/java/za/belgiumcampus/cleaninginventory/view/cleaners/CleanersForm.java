    private void btnAddCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCActionPerformed
    try {
        Cleaner cleaner = new Cleaner();

        cleaner.setFirstName(txtFirstName.getText().trim());
        cleaner.setLastName(txtLastName.getText().trim());
        cleaner.setEmail(txtEmail.getText().trim());
        cleaner.setPhone(txtPhone.getText().trim());
        cleaner.setDepartment(cmbDepartment.getSelectedItem().toString());
        cleaner.setIsActive(chkActive.isSelected());
        
        if (!Validation.isValidDepartment(cmbDepartment.getSelectedItem().toString())) {
    JOptionPane.showMessageDialog(this, "Please select a department.");
    return;
}

        if (cleanerController.addCleaner(cleaner)) {
            JOptionPane.showMessageDialog(this, "Cleaner added successfully.");
            loadCleaners();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add cleaner.");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnAddCActionPerformed

