    private void loadInventoryReportTable(List<InventoryReport> list) {
        String[] columns = {"Material ID", "Material Name", "Quantity Available"};
        tableModel = new DefaultTableModel(columns, 0);

        for (InventoryReport r : list) {
            tableModel.addRow(new Object[]{
                r.getMaterialId(),
                r.getMaterialName(),
                r.getQuantityAvailable()
            });
        }

        tblReports.setModel(tableModel);
    }

