    private void btnStocklowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStocklowActionPerformed
    try {
            if (!Validation.isPositiveInteger(txtThreshold.getText())) {
                JOptionPane.showMessageDialog(this, "Threshold must be a number.");
                return;
            }

            int threshold = Integer.parseInt(txtThreshold.getText());
            List<LowStockReport> list = reportsController.getLowStockReport(threshold);
            loadLowStockReportTable(list);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnStocklowActionPerformed

